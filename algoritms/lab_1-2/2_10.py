def f(n):
	sum = 0
	for i in range(1, n + 1):
		sum = sum + i
	return sum


'''1. У коді є один цикл for, який проходить від 1 до n. На кожній ітерації виконується операція додавання (sum = sum + i).
Оскільки ця кількість прямо пропорційна розміру вхідного числа n, асимптотична складність цієї функції у найгіршому
випадку становить O(n).

2. Результат: сума натуральних чисел від 1 до n.

3. Оптимізація: використання готової формули n(n+1)/2 => оцінка часу стане O(1).'''
